public class ex_85 {
    public static void main(String[] args) {
        System.out.println(java.lang.Math.max(3.14, 3.14159));  // 3.14159
        System.out.println(java.lang.Math.min(3.14, 3.14159));  // 3.14

        System.out.println(java.lang.Math.max(-10, -11));       // -10
        System.out.println(java.lang.Math.min(-10, -11));       // -11
    }
}
